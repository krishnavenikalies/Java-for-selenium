package Arrays;

import interfaces.interfaceexam;

public class arrayexam {

	public static void main(String[] args) {
		int a[][]= {{2,4,5},{3,4,7}};
		
		for(int i=0;i<=1;i++)
		{
			for(int j=0;j<=2;j++)
			{
				System.out.print(a[i][j]);
				System.out.print("\t");
				
			}
			System.out.println("");
		}
		
	}

}
